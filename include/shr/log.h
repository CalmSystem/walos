enum w_log_level {
	WL_EMERG, /* Emergency - System is unusable */
	WL_ALERT, /* Alert - Action must be taken immediately */
	WL_CRIT, /* Critical - System is unstable */
	WL_ERR, /* Error - Something went wrong */
	WL_WARN, /* Warning - Danger may cause problems */
	WL_NOTICE, /* Notice - Normal but significant conditions */
	WL_INFO, /* Informational - Informational messages */
	WL_DEBUG /* Debug - Debug-level messages */
};
